export async function fetchAllUsers(page = 1, limit = 8) {
  const start = (page - 1) * limit;
  const end = start + limit - 1;
  const { data, error } = await supabase
    .from("guests")
    .select("*")
    .range(start, end);

  if (error) {
    console.error(error);
    throw new Error("Fetching all users are not possible");
  }
  return data;
}
